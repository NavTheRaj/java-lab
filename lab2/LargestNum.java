class LargestNum {
    public static void main(String[] args) {

        int value1 = 15;
        int value2 = 15;

        if (value1 > value2) {
            System.out.println(value1 + " is greater");

        } else if (value1 == value2) {
            System.out.println("Both are equal");
        } else {
            System.out.println(value2 + " is greater");
        }
    }
}